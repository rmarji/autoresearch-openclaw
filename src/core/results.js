import fs from 'fs-extra';
import path from 'path';
import { CONFIG_DIR, improvementPct } from '../utils/config.js';

const RESULTS_DIR = path.join(CONFIG_DIR, 'results');

function validateSessionName(name) {
  if (!name || typeof name !== 'string') throw new Error('Session name is required');
  if (name.includes('..') || name.includes('/') || name.includes('\\')) {
    throw new Error(`Invalid session name: ${name}`);
  }
}

export async function createSession(sessionName) {
  validateSessionName(sessionName);
  const sessionDir = path.join(RESULTS_DIR, sessionName);
  await fs.ensureDir(sessionDir);

  const tsvHeader = 'iteration\tmetric_before\tmetric_after\tkept\tchange_desc\n';
  await fs.writeFile(path.join(sessionDir, 'results.tsv'), tsvHeader);

  return sessionDir;
}

export async function appendResult(sessionDir, entry) {
  const line = `${entry.iteration}\t${entry.metricBefore.toFixed(4)}\t${entry.metricAfter.toFixed(4)}\t${entry.kept ? 'kept' : 'reverted'}\t${entry.hypothesis}\n`;
  await fs.appendFile(path.join(sessionDir, 'results.tsv'), line);
}

export async function appendHypothesis(sessionDir, entry) {
  const line = JSON.stringify({
    iteration: entry.iteration,
    hypothesis: entry.hypothesis,
    metricBefore: entry.metricBefore,
    metricAfter: entry.metricAfter,
    kept: entry.kept,
    timestamp: new Date().toISOString()
  }) + '\n';
  await fs.appendFile(path.join(sessionDir, 'hypothesis_log.jsonl'), line);
}

export async function writeReport(sessionDir, data) {
  const { sessionName, file, metric, goal, budget, baseline, bestMetric, iterations } = data;

  const improvementRaw = improvementPct(baseline, bestMetric, goal);
  const improvement = improvementRaw === Infinity ? '∞' : improvementRaw.toFixed(1);

  const kept = iterations.filter(i => i.kept).length;
  const reverted = iterations.length - kept;

  const rows = iterations.map(i =>
    `| ${i.iteration} | ${i.metricBefore.toFixed(4)} | ${i.metricAfter.toFixed(4)} | ${i.kept ? '✓ kept' : '✗ reverted'} | ${i.hypothesis} |`
  ).join('\n');

  const report = `# AutoResearch Report: ${sessionName}

## Summary
- **File:** ${file}
- **Metric:** ${metric}
- **Goal:** ${goal}
- **Budget:** ${budget} iterations
- **Baseline metric:** ${baseline.toFixed(4)}
- **Best metric:** ${bestMetric.toFixed(4)}
- **Improvement:** ${improvement}%
- **Kept:** ${kept} changes
- **Reverted:** ${reverted} changes

## Iterations

| # | Before | After | Result | Change |
|---|--------|-------|--------|--------|
${rows}

---
Generated by [AutoResearch OpenClaw](https://github.com/rmarji/autoresearch-openclaw)
`;

  await fs.writeFile(path.join(sessionDir, 'report.md'), report);
}

export async function writeBest(sessionDir, content) {
  await fs.writeFile(path.join(sessionDir, 'best.md'), content);
}

export async function listSessions() {
  if (!await fs.pathExists(RESULTS_DIR)) return [];

  const dirs = await fs.readdir(RESULTS_DIR);
  const sessions = [];

  for (const dir of dirs) {
    const sessionPath = path.join(RESULTS_DIR, dir);
    const stat = await fs.stat(sessionPath);
    if (!stat.isDirectory()) continue;

    const session = { name: dir, date: stat.mtime, path: sessionPath };

    const tsvPath = path.join(sessionPath, 'results.tsv');
    if (await fs.pathExists(tsvPath)) {
      const content = await fs.readFile(tsvPath, 'utf8');
      const lines = content.trim().split('\n').slice(1);
      session.iterations = lines.length;
      if (lines.length > 0) {
        const firstLine = lines[0].split('\t');
        const lastKeptLine = [...lines].reverse().find(l => l.includes('kept'));
        session.startMetric = parseFloat(firstLine[1]);
        if (lastKeptLine) {
          session.endMetric = parseFloat(lastKeptLine.split('\t')[2]);
        } else {
          session.endMetric = session.startMetric;
        }
        session.kept = lines.filter(l => l.split('\t')[3] === 'kept').length;
      }
    }

    sessions.push(session);
  }

  return sessions.sort((a, b) => b.date - a.date);
}

export async function getSession(name) {
  validateSessionName(name);
  const sessionDir = path.join(RESULTS_DIR, name);
  if (!await fs.pathExists(sessionDir)) return null;

  const result = { name, path: sessionDir };

  const tsvPath = path.join(sessionDir, 'results.tsv');
  if (await fs.pathExists(tsvPath)) {
    result.tsv = await fs.readFile(tsvPath, 'utf8');
  }

  const reportPath = path.join(sessionDir, 'report.md');
  if (await fs.pathExists(reportPath)) {
    result.report = await fs.readFile(reportPath, 'utf8');
  }

  const bestPath = path.join(sessionDir, 'best.md');
  if (await fs.pathExists(bestPath)) {
    result.best = await fs.readFile(bestPath, 'utf8');
  }

  return result;
}
